            <p style="margin: 0 17 5 63">Dalam kegiatan identifikasi permasalahan lingkungan berdasarkan pelaporan masyarakat atas dugaan pencemaran dan/atau kerusakan lingkungan sebagai berikut :
                Latar Belakang Dugaan Pencemaran 
                Berikut adalah latar belakang terjadinya pelaporan masyarakat atas dugaan pencemaran dan/atau kerusakan lingkungan :
                Pelapor atas nama Bapak Phimadewi dengan alamat Jalan Wongso RT 01 RW 06 Pagergunung Desa Gunungsari Kecamatan Bumiaji Kota Batu pada awalnya melaporkan atas pencemaran dari asap pembakaran kayu pada proses produksi tahu dari usaha kecil UD Tahu Baru milik Bapak Yantok dengan alamat Jalan Wongso RT 01 RW 06 Pagergunung Desa Gunungsari Kecamatan Bumiaji menggunakan media online pengaduan melalui alamat : https://awase.dlh.batukota.go.id
                Pengadu mengeluhkan terkait asap pembakaran kayu karena cerobong asap dari tungku pembakaran pecah sehingga terdapat banyak lubang di sepanjang cerobong asap yang menyebabkan asap keluar dan langsung berhadapan dengan rumah tempat tinggal pengadu;
                Dari pengaduan online yang masuk kemudian dilakukan tindak lanjut oleh Dinas Lingkungan Hidup berupa verifikasi lapangan pada Selasa, 7 Maret 2023;
                Pihak yang dilaporkan adalah Yantok, pemilik UD Tahu Baru yang bertempat tinggal dam memiliki usaha produksi tahu tepat di depan rumah pengadu . Dugaan pencemaran berasal dari asap pembakaran kayu melalui tungku pembakaran yang digunakan pada pemasakan tahu;
                UD Tahu Baru telah berdiri sejak 18 tahun yang lalu, dan saat ini dikelola oleh generasi ke 2 dari pemilik tahu awal. Kapasitas produksi ±100 kg kedelai per hari, dengan kurun waktu produksi mulai jam 11 sampai jam 14
                
                
                Permasalahan Lingkungan yang Mendasari 
                Tempat produksi tahu berada di samping dan depan rumah Pak Yantok pemilik usaha tahu dan berhadapan langsung dengan pengadu;
                Kondisi ruang pembakaran berukuran 3 meter x 4 meter sedangkan ruang produksi berukuran 8 meter x 4 meter dilengkapi dengan sarana dan prasarana produksi tahu seperti bak perebusan, bak pembuatan dan bak penyaringan;
                Proses produksi tahu dimulai dari 
                 FOTO
                UD Tahu Baru memproduksi tahu mentah dan tahu matang goreng dengan proses produksi mulai dari pencucian kedelai, perendaman, penumbukkan dan pemanasan pada ketel dan penyaringan sari kedelai, penambahan bahan tambahan cuka yang diproduksi sendiri dari fermentasi kulit kedelai dan kemudian dicetak menjadi tahu kotak. Sedangkan untuk tahu matang dilakukan penggorengan tahu mentah sehingga menjadi tahu goreng siap konsumsi
                Semua proses produksi tahu dilakukan menggunakan bahan bakar energy pembakaran kayu yang dibakar pada tungku pembakar yang dihubungkan dengan cerobong pelepas asap dengan ketinggian 3 meter dari tungku. Proses produksi dimulai pada pukul 11.00 sampai 14.00 dengan jumlah bahan bakar kayu rata-rata mencapai 2 m3. Jenis kayu yang digunakan beranekaragam, seperti kayu sisa material, kayu dan ranting-ranting serta kayu sisa atau bekas produksi peralatan rumah tangga
                
                Hasil Identifikasi Masalah 
                Hasil identifikasi masalah dari verifikasi lapangan terkait dugaan pencemaran dari   asap pembakaran sumber energy pembuatan tahu UD Tahu Baru adalah sebagai berikut :
                Asap pembakaran kayu dari proses produksi tahu UD Tahu Baru berlangsung selama proses pembuatan yaitu pukul 11.00-14.00
                Pembakaran kayu dilakukan pada tungku pembakaran dan dilepaskan menggunakan cerobong asap setinggi ±3 meter dari tungku 
                Sudah ada upaya dari UD Tahu Baru untuk menyalurkan asap melalui cerobong namun kondisi cerobong yang terlalu pendek dan dalam kondisi berlubang dan pecah mengakibatkan asap keluar dan menyebar horizontal 
                Sudah ada komitmen dari pemilik usaha tahu untuk melakukan perbaikan kondisi cerobong dengan penggantian cerobong menggunakan bahan yang lebih kuat seperti stainless steel
                Verifikasi lapangan didampingi oleh Kepala Dusun Pagergunung yang akan melakukan pemantauan penggantian cerobong dan melakukan komunikasi lebih lanjut dengan Dinas Lingkungan Hidup untuk selanjutnya dilakukan evaluasi 
                
                Upaya Solusi yang Ditawarkan 
                Solusi atau pemecahan masalah yang ditawarkan oleh Pengendali Dampak Lingkungan adalah :
                Perlunya dilakukan upaya penggantian cerobong dengan ketinggian minmal 6 (enam) meter dan terbuat dari bahan yang tahan panas
                 Perlunya dilakukan pemasangan penyaring pada cerobong asap sehingga dapat menahan fly ash agar tidak terlepas ke udara dan lingkungan sekitar 
                Perlunya dipasang spray air sehingga dapat menangkap fly ash dan bottom ash agar partikulat tidak mencemari udara sekitar
                
            </p>
