        {{-- // VI. IDENTIFIKASI MASALAH (KOSONGKAN) --}}
        <div class="paragraph" class="jarak"><br>
            <table border="0" width="650px">
                <tr>
                    <td width="14"></td>
                    <td width="42">VI.</td>
                    <p style="margin: 0 0 0 0">Identifikasi Masalah</p>
