            Keberadaan industri kecil skala rumah tangga tidak lepas dari limbah atau buangan yang dihasilkan baik dari operasional produksi maupun sisa bahan baku dan bahan tambahan. Hal inilah yang perlu menjadi perhatian khusus mengingat industri kecil skala rumah tangga berdiri dengan modal kecil dan pemahaman terkait pengelolaan limbah dan sampah yang terbatas. Edukasi dan literasi terkait penanganan limbah skala industri kecil ini menjadi tanggungjawab Pemerintah, di mana pembinaan dan pendampingan bahkan penyediaan sarana prasarana pengelolaan limbah dapat difasilitasi. 
